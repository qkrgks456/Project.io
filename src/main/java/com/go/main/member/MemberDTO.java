package com.go.main.member;

public class MemberDTO {
	
	private String id;
	private String pw;
	private String email;
	private String name;
	private String phone;
	private String gender;
	private String position;
	private int age;
	private String date;
	private String emailcheck;
	private String crowdedcheck;
	private String resigncheck;	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getEmailcheck() {
		return emailcheck;
	}
	public void setEmailcheck(String emailcheck) {
		this.emailcheck = emailcheck;
	}
	public String getCrowdedcheck() {
		return crowdedcheck;
	}
	public void setCrowdedcheck(String crowdedcheck) {
		this.crowdedcheck = crowdedcheck;
	}
	public String getResigncheck() {
		return resigncheck;
	}
	public void setResigncheck(String resigncheck) {
		this.resigncheck = resigncheck;
	}
	
}
